package com.hamit.dersler;

public class Ders_013_Math {

	public static void main(String[] args) {
		System.out.println(Math.PI);
		System.out.println(Math.E);
		System.out.println(Math.min(4, -85));
		System.out.println(Math.max(4, -85));
		System.out.println("mutlak sayı: " + Math.abs(-144));
		System.out.println("karekök sayı: " + Math.sqrt(Math.abs(-144)));
		System.out.println(Math.pow(2, 5));
		System.out.println(Math.floor(4.9));
		System.out.println(Math.ceil(4.1));

		System.out.println(Math.round(4.5));
		System.out.println(Math.round(4.4));

	}

}
