package com.hamit.dersler;

public class Ders_006_bilimsel {

	public static void main(String[] args) {

		double bilimsel1 = 123400;
		System.out.println(bilimsel1);

		// E+2= 2 tane sıfır sağa kaydır
		double bilimsel2 = 1234E+2;
		System.out.println(bilimsel2);

		// E-2= 2 tane sıfır sola kaydır
		double bilimsel3 = 1234E-2;
		System.out.println(bilimsel3);

	}

}
